                          <p>Can you develop diabetes during pregnancy, even if you didn’t have same before pregnancy?
                            This is the question that may arise in your mind and can dampen your mood. The journey to
                            motherhood, i.e. pregnancy can have its own problem. A woman may not have diabetes before
                            pregnancy but may develop during pregnancy. </p>

                          <p>Pregnancy is a beautiful gift of nature to women, that translates into motherhood. Being
