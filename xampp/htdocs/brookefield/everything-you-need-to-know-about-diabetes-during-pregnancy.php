                          <p>Diabetes that occur during pregnancy is called Gestational diabetes. It disappears, once
                            the baby is born. Gestational diabetes usually occurs in the second half of the pregnancy
                            and is quite common in India. Out of 100, around 18 women may suffer from Gestational
                            Diabetes.</p>

                          <p><strong>What may be the cause of Gestational diabetes?</strong></p>
                          <p>Your body needs to work more during pregnancy as you are nurturing another life in your
                            body. The pancreas produces insulin, a hormone that works on the glucose level of the body.
                            During pregnancy the placenta produces a hormone that interferes with the body’s ability to
                            use insulin. Hence, there is more glucose in blood than normal. Such situation causes
                            Gestational diabetes in pregnant women.</p>

                          <p>So, either you need to reduce the glucose content in the blood or induce additional insulin
                            to control the glucose level. By adopting some exercises and healthy food habit choices, you
                            can control the glucose level without the need of medicines. </p>

                          <p>What are the common symptoms of Gestational diabetes?</p>

                          <p>You may often confuse the symptoms of Gestational diabetes, as normal features during
                            pregnancy. Like feeling tired, thirsty, hungry and need to pee more often than normal, or
                            weight gain. It is diagnosed only during routine screening tests or by checking the blood
                            sugar levels.</p>

                          <p>Its best to get your urine and blood sugar checked regularly and more often during the
                            second half of the pregnancy. Early diagnosis can help curb the problem, before it gets
                            complicated.</p>

                          <p>What are the complications that can occur due to Diabetes during pregnancy?</p>

                          <p>If the diabetes is not diagnosed or left untreated it may cause serious complications. The
                            complication can result in</p>

                          <ol>
                            <li>High blood pressure</li>
                            <li>Frequent urinary infection</li>
                            <li>Preeclampsia</li>
                            <li>Can cause complications during delivery</li>
                            <li>Can risk the life of both mother and baby</li>

                          </ol>
                          <p>Since gestational diabetes occurs in the later stage of pregnancy, it needs to be checked
                            immediately. The babies begun to gain weight due to it and can be around 10 lbs at the time
                            of delivery. The large baby poses problem during delivery and may need to go for a
                            C-section. </p>

                          <p>The extra glucose in the blood is delivered to the baby causing high blood glucose levels
                            in the baby. The baby’s pancreas then releases more insulin to balance the blood sugar
                            levels. The extra glucose is stored as fat making the baby big.</p>

                          <p>The baby on the other hand may suffer some complications like respiratory distress syndrome
                            or low blood sugar levels, also known as Hyperglycaemia; after birth. The baby may also have
                            the risk of developing type 2 diabetes in later stage of life.</p>

                          <p>Who can be at the risk of Gestational diabetes?</p>
                          <ol>
                            <li>Women who are overweight, must control their weight to prevent the risk of Gestational
                              diabetes.</li>
                            <li>Women with the family history of type 2 diabetes are at risk of developing Gestational
                              diabetes.</li>
                            <li>Pregnancy after 25 years of age, may pose some risk.</li>
                          </ol>

