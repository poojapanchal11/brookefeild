                          <h2>What is Diabetes, its symptoms and precautions? </h2>



                          <p>Do you know approximately 72 million cases of Diabetes was recorded in 2017, and by 2025 it
                            is expected to double. Diabetes commonly referred as a “lifestyle disease” is the fastest
                            growing disease in India. What does it mean? It means that we must take some good actions,
                            before it impacts the coming generations. Before we save our society, we, must understand
                            -What is Diabetes, its symptoms and precautions?</p>
                          <p>In general term, Diabetes is a lifestyle disease that is caused by lack of physical
                            activity and over consumption of high calorie and fast foods. Ever imagined a society like
                            India could be consumed by a disease, because of surplus availability of money, food and
                            lifestyle ease. No! Well Diabetes knocks Indians 10 years earlier than the rest of the
                            world. Isn’t it shocking….?</p>

                          <p>Diabetes in medical term is called ‘diabetes mellitus’. It is a chronic metabolic disease,
                            described by presence of high blood sugar levels in the blood, resulting from either
                            shortage in insulin secretion, or its performance or may be both. Diabetes was first
                            recognized as a disease related with ‘sweet urine’ and excessive muscle loss. Increased
                            levels of blood sugar pushes sugar out into the urine, hence the term ‘sweet urine’.</p>
                          <p>What is Glucose?</p>
                          <p>While we are talking about diabetes, we keep mentioning glucose. And at once we feel that
                            the glucose is the devil in getting you diabetes. Hmm…. let’s know more about the devil -the
                            glucose….! Glucose is the simple sugar present in the food. Glucose is an important nutrient
                            that furnishes energy for appropriate functioning of the body cells. Carbohydrates the basic
                            nutrient in food, is broken down in the small intestine. The glucose released from the
                            broken food is transferred in to the bloodstream, which transports to all the cells in the
                            body where it is used.</p>
                          <p>In normal condition, the sugar levels are controlled and managed by insulin. The insulin
                            transfers the glucose to other body cells. Glucose is an important source of energy to body.
                            All the functions of the body are performed by the energy released by glucose. So, glucose
                            is not the devil, but it’s over presence is, which the body is not able to utilize, and
                            hence passes it out.</p>
                          <p><strong>What is insulin?</strong></p>
                          <p>Insulin is the hormone released by pancreas, that maintains the balance of glucose level in
                            blood and helps in transportation of glucose to other body cells. As the food goes in,
                            insulin is released to work on it.</p>
                          <p>Glucose needs the help of insulin to reach the cells. Without insulin, the cells cannot get
                            glucose, and are deprived of the energy source regardless of presence of generous glucose in
                            the bloodstream. That is why you feel lack of energy and weight loss.</p>
                          <p>In diabetes you need to take insulin from outside in form of tablets or insulin injections,
                            to maintain glucose level.</p>
                          <p><strong>Why diabetes is called a chronic disease?</strong></p>
                          <p>Once you have it, you have it lifelong. You cannot get rid of it in your life time, like
                            your undeterred spouse. You can control it, manage it, but it stays persistent. So, what can
                            you do now? You can save your future generation and family. Do you know you can pass
                            diabetes to your children? So, work before its too late. Bring in a life style change and
                            prevent diabetes to enter your family.</p>
                          <p><strong>What are the symptoms of diabetes?</strong></p>
                          <p>Every disease has some warning signs or symptoms, which help us to recognize it. </p>
                          <ol>
                            <li>Frequent urination- the elevated blood sugar, pushes glucose out through urine, leading
                              to frequent urination.</li>
                            <li>Increases thirst- the frequent urination leads to loss of water in the body, leading to
                              dehydration and feeling of thirst.</li>
                            <li>Increased hunger- again when the body is throwing glucose out, your body feels lack of
                              energy, that causes hunger pangs. </li>
                            <li>Extreme fatigue- If you don’t eat you immediately, you feel dizzy and faint. Your body
                              feel tired all the time, because it is not able to pass glucose to your body cells.</li>
                            <li>Blur vision- fluctuation/variation in blood sugar may cause blurred vision.</li>
                            <li>Sores that don’t heal- if you get a cut or wound, it will not heal quickly. The
                              functions of body becomes defected with the presence of high sugar in blood.</li>
                            <li>Weight loss- you may experience a sudden weight loss.</li>
                          </ol>
                          <p>Although these are common symptoms of diabetes, some symptoms are particular to men &
                            women.</p>
