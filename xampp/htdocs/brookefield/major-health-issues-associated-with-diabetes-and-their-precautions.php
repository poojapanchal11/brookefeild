                          <h2>Major health issues associated with Diabetes and their precautions </h2>
                          <p>Are we taking diabetes seriously? No…I don’t think so. The alarming increase in diabetes
                            shows our ignorance towards the major health issues that can crop up. Our vital organs need
                            energy to function, and poor management of diabetes hinders the flow of oxygen and glucose
                            to these organs, leading to failure or cramping of these organs.</p>

                          <p>Scared! Let me alarm you towards the major health issues associated with diabetes and their
                            precaution.</p>

                          <p><strong>What could be the long-term impact of diabetes?</strong></p>

                          <p>Your heart can be impacted by diabetes, leading to stroke, heart attack, blood vessel
                            disease. The effect doubles if you also have high cholesterol and blood pressure. You can
                            save your heart if you follow some golden rules-</p>

                          <ol>
                            <li>Check your blood pressure every 6 months. If you are under medication to control blood
                              pressure, then you must check it every month.</li>
                            <li>Take HbA1c test twice/thrice a year. It will give a clear status of your blood sugar
                              levels.</li>
                            <li>Get your cholesterol checked at least once a year. ECG, lipid profile and exercise
                              stress test should also be taken.</li>


                          </ol>
                          <p>Your Eyes can be impacted by diabetes leading to many complications like-</p>

                          <p><strong>Retinopathy-</strong></p>
                          <p>The blood vessels in and around retina gets damaged leading to poor eye sight, blurry
                            visions, etc. Its best to get a complete and regular diabetic eye check- up, since there are
                            no symptoms of retinopathy in early stages.</p>
                          <p><strong>Macular Oedema-</strong></p>
                          <p>When the blood vessels in retina get damaged, it leads to swelling in macula, causing
                            accumulation of fluid. Macula is a part of retina, that assist you to see objects clearly.
                            This results in blurry vision. It can be treated but must be detected early.</p>

                          <p><strong>Cataracts</strong></p>
                          <p>Cataract is a condition, where the lens of the eye become foggy/cloudy. This can lead to
                            foggy, distorted vision and your eyes will become sensitive to harsh lights. You may develop
                            cataract at an early stage of diabetes.</p>

                          <p><strong>Glaucoma </strong></p>

                          <p>In Glaucoma, the fluid within the eye develops high pressure level, that slowly damages the
                            eye. However, Glaucoma can occur to people without diabetes also.</p>

                          <p>You must hence take regular, i.e. once a year eye check- up.</p>

                          <p>Your kidney is at a high risk with diabetes. Kidney disease like Nephropathy develops,
                            which can be taken care of with the right treatment. Since it is painless, and does not show
                            any symptoms, it can be left unattended, leading to major problems. So, its best to go for
                            screening, where it can be diagnosed by the presence of microalbumin- a protein that
                            disturbs glucose absorption. The doctor can check your kidney function and e-GFR with a
                            blood test. E-GFR is estimated glomerular filtration test.</p>

                          <p>ACE inhibitor and Angiotensin receptor antagonists’ tablet can cushion kidney from further
                            damage.</p>

                          <p>Your nerves- the sensory and motor nerves of the arms & hands; legs & feet; chest & stomach
                            and the internal nerves that regulates the actions of body organs can get damaged or fail to
                            perform.</p>

                          <p>However, you can prevent nerve damage by-</p>

                          <ul>
                            <li>Keeping your blood sugar levels within the target range.</li>
                            <li>Don’t smoke</li>
                            <li>Drink alcohol within the recommended guidelines.</li>
                            <li>Consult your doctor immediately, if you have any problems with hands, arms, legs or
                              feet, your stomach, bowels or bladder.</li>


                          </ul>
                          <p>Your foot can experience low blood supply and nerve functions, that can lead to</p>
                          <ul>
                            <li>Delay in healing</li>
                            <li>high risk of infection</li>
                            <li>reduce feeling or numbness in feet</li>
                            <li>ulcer and </li>
                            <li>structural foot problems like clawed toes can occur</li>
                          </ul>

                          <p>However, you can take care of your feet by</p>
                          <ul>
                            <li>visiting a podiatrist yearly once. They can check the health of your feet by examining
                              the nerve function and blood supply and note any change in the structure of your feet.
                            </li>
                            <li>Examining your feet everyday for any cuts, calluses, corns, blisters tinea and any
                              structural change. If noticed and treated early can prevent fatal consequences.</li>
                            <li>Moisturizing your feet well on regular basis</li>
                            <li>Wearing medical or comfortable footwear. </li>
                          </ul>

                          <p>Your skin protects your body from outside environment. Because of diabetes the small nerves
                            and blood vessels near the skin is damaged, causing dryness, patchy skin, and
                            hyper-sensitivity to any infection.</p>
                          <p>So, you need to prevent your skin from any cut and damage. You must moisturize your skin
                            well and protect it from over exposure from heat, cold etc.</p>
                          <p>Your teeth and gums become more sensitive and are at a risk of gum infection and tooth
                            decay. Visit the dentist regularly, maintain oral hygiene and brush your teeth twice a day.
                          </p>
                          <p>You may develop mental stress. You may develop anxiety, depression and poor management of
                            your emotions.</p>
                          <p>You may face Surgery risk. </p>
                          <p>Since your body has low glucose absorption you may face problems and additional risk during
                            surgery. Here are the lists of risk that you may face-</p>
                          <ul>
                            <li>Low blood glucose (hypoglycaemia) or high blood glucose (hyperglycaemia) after surgery.
                            </li>
                            <li>Poor or slow healing of wound or cut from surgery.</li>
                            <li>Weak tissue and skin around the surgery area.</li>
                            <li>Infection in the wound</li>
                            <li>Other infections like pneumonia, sepsis, or urinary tract infections.</li>
                            <li>HHNK- Hyperglycaemic Hyperosmolar Nonketotic Syndrome</li>
                            <li>DKA- Diabetic Ketoacidosis</li>
                            <li></li>
                          </ul>
