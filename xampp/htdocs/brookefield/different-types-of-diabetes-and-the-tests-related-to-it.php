                          <p><strong>Type 1</strong></p>
                          <p>In type 1 diabetes the pancreas does not produce any insulin. This means that the body is
                            totally dependent on the insulin supplied from outside sources. That is why it is called
                            Insulin dependent diabetes mellitus (IDDM) or juvenile onset-diabetes mellitus.</p>
                          <p><strong>What is the cause of type 1- IDDM?</strong></p>

                          <p>In this case your immune system attacks the pancreas and destroys the cells needed to make
                            insulin. This condition is called autoimmune reaction, as your body attacks itself. This
                            results in no insulin or very low insulin production, making the body completely depend on
                            insulin medication for survival.</p>
                          <p>It’s called juvenile onset-diabetes mellitus, since it hits the young, lean people of age
                            below 30 years. Just a 10% suffer from type 1 and 90 % suffer from type 2 of all the
                            diabetic population. </p>
                          <p>Type 1 diabetes may be caused by exposure to certain infections- viral or bacterial;
                            chemical toxins in food/environment; or unidentified elements causing autoimmune reaction.
                            Some of the anti-bodies detected in type 1 are</p>
                          <li>1. Anti-islet cell antibodies</li>
                          <li>2. Anti-insulin antibodies</li>
                          <li>3. Anti-glutamic decarboxylase antibodies</li>
                          <p>They can be identified in the individuals, suffering from type 1 diabetes, and can be
                            treated. It could also be hereditary or genetic and the individual can be a carrier of genes
                            related to type 1 diabetes.</p>
                          <p><strong>Type 2</strong></p>
                          <p>Type 2 diabetes, also popularly known as NIDDM- non-insulin dependent diabetes mellitus or
                            adult -onset diabetes mellitus (AODM). In this type the body can produce insulin, but-</p>
                          <li>1. It is not enough for the body, hence need to take from external sources</li>
                          <li>2. Lack of sensitivity to insulin; meaning the insulin is not effectively working on
                            cells, specially the fat and muscle cells.</li>
                          <p>This results in high glucose level in blood, which is controlled by medicines and insulin.
                          </p>
                          <p><strong>What are the causes of type 2 diabetes?</strong></p>
                          <li><strong>1. Hereditary </strong></li>
                          <p>One of the most common cause of diabetes. If you have a parent with diabetes, you are
                            likely to be diabetic. So, if it runs in your family, plan and adopt a healthy lifestyle to
                            avoid it.</p>
                          <li><strong>2. Obesity </strong></li>
                          <p>Obese does not always mean unhealthy, but it can trigger diabetes. Replace complex food
                            with simple food, to help your body overcome hunger pangs and keep you full for longer
                            hours.</p>
                          <li><strong>3. Poor dietary habits.</strong></li>
                          <p>Where do we and our children eat? Near the T.V; when you eat in front of the T.V. your mind
                            does not register the intake of food, and you tend to over eat. You don’t chew your food
                            properly and just gobble down, creating a mess in your stomach.</p>
                          <p>What you consume? All, ready to eat white foods, Fried and high sugar foods, which is good
                            for your taste buds than your stomach.</p>
                          <li><strong>4. Sedentary lifestyle</strong></li>
                          <p>As you grow older, your lifestyle changes. You no longer make more physical effort, but the
                            food habits do not change. We consume more than we release or use. This causes extra deposit
                            of glucose in the body. You need to burn that glucose. Forget the age and keep your self on
                            toes. Try changing, shifting, don’t stay at one place for too long. Moving will keep your
                            body active. Take some exercise or yoga.</p>

                          <p><strong>Tests for Type 1 & Type 2 diabetes</strong></p>
                          <li><strong>• The haemoglobin A1c test</strong></li>
                          <p>It’s a simple blood test called glycosylated haemoglobin in medical term. Blood is drawn
                            from a vial from your arm. This test detects your average blood sugar level for the past 3
                            months. This test does not require fasting. If the result is which is measured in percentage
                            is-</p>
                          <p> Less than 5.7 %, it is normal</p>
                          <p> Between 5.7% - 6.4 %, you could be prediabetic.</p>
                          <p> Equal to or greater than 6.5% indicates you are diabetic.</p>
                          <li><strong>Random Blood sugar test</strong></li>
                          <p>The blood test is conducted at any time of the day regardless of the time you ate last. If
                            the result shows blood sugar level higher than 11.1 millimoles per litre, then it suggests
                            that you have diabetes.</p>

                          <li><strong>Fasting blood sugar test</strong></li>
                          <p>As the name suggest, blood test is done after an overnight fast. If the Fasting blood sugar
                            level is less than 100mg/dL, you are normal. if the fasting blood sugar level is between
                            100-125 mg/dL, then you may be prediabetic. However, if your blood sugar level is 126mg/dL
                            or higher, then you have diabetes.</p>
                          <p>The test is conducted to see the amount of glucose in blood against the normal figures. As
                            per the result the medication is prescribed, to normalise the glucose level in blood.</p>
                          <li><strong>Oral glucose tolerance test</strong></li>
                          <p>This is the next step of fasting blood sugar. You are asked to eat and drink sugary foods.
                            The blood sugar is tested after two hours of consuming food. If the blood sugar level is
                            less than 140mg/dL then you are normal. Between 140- 199 mg/dL- prediabetic, and more than
                            200mg/dL diabetic.</p>
                          <p>This test is done to know the functioning of insulin, and how much sugar it can
                            handle/tolerate. As per the result you may be administered medication and diet.</p>
