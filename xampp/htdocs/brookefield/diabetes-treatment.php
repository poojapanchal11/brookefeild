                          <h3>Complications of diabetes</h3>
                          <p>Insulin is the hormone necessary to carry glucose (produced from the foods you eat) from
                            the bloodstream into the body’s cells, where it is used for energy. When there is not enough
                            insulin, glucose builds up in the blood, putting people at risk for serious health problems
                            including:</p>
                          <ol>
                            <li>Heart attacks and stroke</li>
                            <li>Kidney problems</li>
                            <li>Numbness in the feet and sores that don’t heal</li>
                            <li>Vision problems</li>
                          </ol>
                          <p>You can help prevent or delay the long-term complications of diabetes by keeping blood
                            glucose levels as normal as possible.
                          </p>
                          <h3>Blood sugar tests</h3>
                          <ul>
                            <li><b>Fasting Blood glucose</b>  -The FBG test is a blood test that determines the amount
                              of glucose (sugar) in the blood after an overnight fast (not eating for at least 8 hours).
                            </li>
                            <li><b>Post Prandial Glucose</b> -This blood test is done two hours after taking the meal.
                            </li>
                            <li><b>Random blood sugar</b> - This test can be done at any time during the day, regardless
                              of your mealtime.</li>
                          </ul>
                          <h4>HbA1C test</h4>
                          <ul>
                            <li><i class="fa fa-bookmark-o"></i>HbA1c is person's average blood glucose of last 3
                              months.</li>
                            <li><i class="fa fa-bookmark-o"></i>HbA1c or haemoglobin A1c blood test is also known as the
                              glycated haemoglobin test.</li>
                            <li><i class="fa fa-bookmark-o"></i>HbA1c test can be done at any time irrespective of meal
                              time.</li>
                          </ul>
                          <h4>Lipid Proﬁle</h4>
                          <p>Monitoring and maintaining healthy levels of lipids is important for a diabetes
                            patient.<br>A lipid proﬁle typically includes:</p>
                          <ol>
                            <li>Total cholesterol</li>
                            <li>High-density lipoprotein cholesterol (HDL-C)</li>
                            <li>Low-density lipoprotein cholesterol (LDL-C)</li>
                            <li>Triglycerides</li>
                          </ol>
                          <h4>BP Monitoring</h4>
                          <p>Monitoring your blood pressure is key to controlling high blood pressure. Your blood
                            pressure is recorded as two numbers:</p>
                          <ol>
                            <li>Systolic blood pressure</li>
                            <li>Diastolic blood pressure</li>
                          </ol>
