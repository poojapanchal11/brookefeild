                          <h2>What is Diabetes?</h2>
                          <p>Diabetes means you have too much sugar in your blood. High blood sugar problem starts when
                            your body no longer makes enough of a chemical, or hormone, called insulin.<br> Your body
                            changes much of the food that you eat into a type of sugar called glucose.<br> This sugar
                            travels in your blood to all the cells in your body. Your body cells need the sugar to give
                            you energy.<br> Insulin helps the sugar to move from your blood into your cells. Without
                            insulin, your cells can’t get the sugar they need to keep you energetic.<br> By moving sugar
                            from your blood to your body cells, insulin helps to keep your blood sugar level normal (not
                            too high; not too low).<br>
                              When you don’t have enough insulin to lower high blood sugar levels means you have
                            diabetes.<br> High blood sugar levels can cause serious health problems Diabetes can, and
                            must, be treated.</p>

                          <h3>When you have Diabetes?</h3>
                          <p>Your body does not make insulin at all <br>Your body does not make enough insulin, or the
                            insulin that body makes doesn't work right.<br> Blood sugar levels stay high of you doesn’t
                            have enough insulin to move sugar from your blood into your cells.</p>
                          <h4>Type 1 Diabetes</h4>
                          <p>
                            In this type 1 diabetes, the body cannot make insulin.<br> Type 1 diabetes occurs more often
                            in the children and young adults than in older adults.<br> People with type 1 diabetes must
                            inject insulin to control their blood sugar.
                          </p>
                          <h3>Symptoms of type 1 diabetes</h3>
                          <ul>
                            <li><i class="fa fa-check"></i>Excessive thirst</li>
                            <li><i class="fa fa-check"></i>Frequent urination</li>
                            <li><i class="fa fa-check"></i>A fruity-like smell</li>
                            <li><i class="fa fa-check"></i>Fatigue weakness</li>
                            <li><i class="fa fa-check"></i>Sugar in urine</li>
                            <li><i class="fa fa-check"></i>Excessive weight loss</li>
                          </ul>
