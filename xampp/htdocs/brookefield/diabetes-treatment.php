                          <h3>Test for Eyes ( Retinopathy)</h3>
                          <p>
                            Diabetes increases your risk of retinopathy, glaucoma and other eye related problems.<br>
                            Your doctor may suggest any of the following tests check the health of your eye.
                          </p>
                          <h4>A Dilated eye (or Fundus) examination</h4>
                          <p>In this test, examiner dilates the pupil and see thorugh the pupil to the retina to detect
                            diabetic retinopathy in its early stage.</p>
                          <h4>A Tonometry test </h4>
                          <p>This test is used to measure ﬂuid pressure inside the eye. Diabetic patients should check
                            their eye at least once in a year. </p>
                          <h4>Acuity test</h4>
                          <p>The visual acuity test is used to determine the smallest letters you can read on
                            a standardised chart (Snellen chart) or a card held 20 feet (6 meters) away. </p>
                          <h3>Test for Neuropathy</h3>
                          <p>One in every three diabetes patient suffers from neuropathy.  If you feel numbness, loss of
                            sensation, cold feet, sensitive to touch, burning sensation at your feet then you should go
                            for your foot examination.</p>
                          <h4>Common test available:</h4>
                          <ol>
                            <li>Filament test. Sensitivity to touch may be tested using a soft nylon ﬁbre called a
                              monoﬁlament.</li>
                            <li>Nerve conduction studies.</li>
                            <li>Electromyography (EMG)</li>
                            <li>Quantitative sensory testing.</li>
                            <li>Autonomic testing.</li>
                            .
                          </ol>
                          <p>Diabetic patients should check their feet regularly to avoid foot complication.</p>
                          <h2>Why do I need Insulin?</h2>
                          <p>It is a hormone produced by the pancreas to regulate the levels of glucose in the blood. In
                            absence of insulin, our body cells cannot use glucose as a source of energy.
                            When you have diabetes, your body doesn’t make insulin or the insulin you do make doesn’t
                            work right. Hence, you need external insulin. You can get the insulin you need by injecting
                            it with an insulin pen, a syringe, or insulin pump.</p>
                          <p>Taking insulin will: Help you to control your sugar levels Give you energy Help you to stay
                            healthy </p>
                          <h3>Insulin Secretions in Body</h3>
                          <p>Insulin is secreted during fasting state known as Basal Insulin.<br> The secretion of
                            insulin increases in response to increased blood glucose levels after taking food.<br> This
                            process is affected by diabetes for which insulin from outside needs to be injected. </p>
                          <h3>How should I take Insulin?</h3>
                          <p>Insulin works best when you inject it into the fatty parts of the body just under the
                            skin.<br>Before you leave the clinic, ensure that you know how to: </p>
                          <p>Prepare insulin</p>
                          <p>Inject insulin</p>
                          <p>Rotate injection site</p>
                          <h3>Which are the insulin delivery devices?</h3>
                          <p>There are different devices to deliver insulin into your body through; syringes, insulin
                            pen, insulin pump, and i-Port are different options for insulin delivery. </p>
                          <h4>Syringe</h4>
                          <p>Insulin syringes are different from normal one.<br> They are thinner, have the almost
                            painless needle and come with removable needle guard.<br> The outer side of the syringe is
                            marked with lines to assist you to withdraw correct amount of insulin. </p>
                          <h4>Pen</h4>
                          <p>It is composed of an insulin cartridge (integrated or bought separately) and a dial to
                            measure the dose and is used with disposable pen needles to deliver the dose.<br>Insulin
                            pens offer several signiﬁcant advantages over insulin syringes: Ease of handling Accuracy
                          </p>
                          <h2>Important Things to Remember:</h2>
                          <h3>Eat regular meals</h3>
                          <p>Choose wholegrain bread and cereals.<br> Eat plenty of vegetables, and choose whole fruits
                            instead of fruit juices.<br>  When you look at food labels, look for products that contain
                            at least 3 grams of ﬁbre per serving. </p>
                          <h3>Do not skip meals. </h3>
                          <p>Meals are best spaced 4 to 5 hours apart. </p>
                          <h3>Stay active</h3>
                          <p>Your blood glucose level will improve if you keep active. Check with your doctor before
                            beginning an exercise or walking program. </p>
                          <h3>Lose weight if you are overweight. </h3>
                          <p>Losing weight gradually, even a small amount of weight can help improve your blood glucose
                            level.
                          </p>
                          <h4>Basic Carbohydrate Counting</h4>
                          <p>Carbohydrates are nutrients in food that turn into glucose (sugar) after being digested to
                            provide energy to the body.<br> People with diabetes should have a moderate amount of
                            carbohydrate at each meal, rather than avoiding them completely.<br> Eating the right amount
                            of carbohydrate at each meal will help to keep blood glucose in a healthy range.<br> Below
                            are some basic guidelines for counting and regulating the amount of carbohydrate in the
                            diet.<br>In general, these foods contain carbohydrates: Starchy vegetables: potatoes, sweet
                            potatoes, yams, dried beans, corn, and peas
                            Fruits: fresh, frozen, canned and juice All breads, cereals, pasta, rice and crackers Milk
                            and yogurt Desserts and sweets Most snack foods</p>
                          <h4>Meat and Meat Substitutes (Protein)</h4>
                          <p>Meat and meat substitutes do not raise your blood glucose. If eaten in excess, they can
                            keep the blood glucose elevated for a longer period of time. Also, meats high in fat can
                            raise cholesterol levels and cause weight gain. It is important to make healthy food
                            choices, so choose lean meats such as: Fish Skinless chicken Skinless turkey Reduced-fat
                            peanut butter Low-fat cheese or cottage cheese Pork loin
                          </p>
                          <h4>Fat</h4>
                          <p>Fat does not raise your blood glucose but can keep the blood glucose elevated if eaten in
                            excess. Fat can contribute to heart disease and weight gain. It is important that you limit
                            the amount of fat you eat and choose healthy fats most often. Use less regular salad
                            dressing, cream cheese, sour cream, gravy and nuts. Choose reduced-fat products and limit
                            the size of your portions. Olive and canola oils are healthier fats for your heart </p>
                          <h4>Sodium</h4>
                          <p>Sodium is most commonly known as salt. In the body, sodium helps to regulate blood pressure
                            and body ﬂuids. A healthy diet for people with diabetes would be to limit sodium to less
                            than 1,500 mg daily. </p>
                          <h4>Eat fruits having low glycemic index</h4>
                          <p>Fruits are an important component of a healthy, balanced diet as they are loaded with
                            powerful nutrients and antioxidants that promote health. Many fruits are also high in ﬁber
                            which is not only good for digestion, but also helps to keep blood sugar levels steady and
                            keeps you full for longer.  Fruits with a low glycemic index (GI value of 55 or less) are
                            always the recommended safe choice for diabetics as they contain mainly slow-release carbs
                            which help to regulate blood sugar levels better. Good examples of low-GI fruit are apples,
                            pears, oranges, peaches, plums and strawberries.
                          </p>
                          <h3>It’s important to be active</h3>
                          <p>Physical activity has many beneﬁts.<br>  It can help you control your blood glucose and
                            your weight.<br>  Physical activity can help prevent heart and blood ﬂow problems.<br>  Many
                            people say they feel better when they get regular exercise. </p>
                          <h3>Start with a little.</h3>
                          <p>If you haven’t been doing any physical activity, talk to your healthcare team before you
                            begin.<br>  Walking, working in the yard, and dancing is good ways to start.<br>  As you
                            become stronger, you can add a few extra minutes to your physical activity.<br>  If you feel
                            pain, slow down or stop and wait until it goes away.  If the pain comes back, talk with your
                            healthcare team right away.</p>
